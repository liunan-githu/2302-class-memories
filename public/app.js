function handleUrlRoute() {
    const path = window.location.pathname;
    
    // 检查用户状态
    if (currentUser && currentUser.isBanned) {
        handleUserBanned(currentUser.banReason);
        return;
    }
    
    if (path.startsWith('/video/')) {
        const videoId = parseInt(path.split('/video/')[1]);
        if (videoId && !isNaN(videoId)) {
            openVideoDetail(videoId, false);
            return;
        }
    }
    

    
    if (path === '/upload') {
        if (currentUser) {
            showPage('upload', false);
        } else {
            showToast('请先登录', 'error');
            navigateTo('/login');
        }
        return;
    }
    
    if (path === '/upload/video') {
        if (currentUser) {
            showPage('uploadVideo', false);
        } else {
            showToast('请先登录', 'error');
            navigateTo('/login');
        }
        return;
    }
    
    if (path === '/upload/status') {
        if (currentUser) {
            showPage('uploadStatus', false);
            loadUserContent();
        } else {
            showToast('请先登录', 'error');
            navigateTo('/login');
        }
        return;
    }
    
    if (path === '/upload/photos') {
        if (currentUser) {
            showPage('uploadPhotos', false);
        } else {
            showToast('请先登录', 'error');
            navigateTo('/login');
        }
        return;
    }
    
    if (path === '/classmates') {
        showPage('classmates', false);
        loadClassmates();
        return;
    }
    
    if (path === '/about') {
        showPage('about', false);
        loadAboutPage();
        return;
    }
    
    if (path === '/login') {
        showPage('auth', false);
        showAuthTab('login');
        return;
    }
    
    if (path === '/register') {
        showPage('auth', false);
        showAuthTab('register');
        return;
    }
    
    // 游客可以访问首页
    showPage('home', false);
}

