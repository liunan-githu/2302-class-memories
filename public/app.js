// 加载用户上传的内容（视频+图片）
async function loadUserContent() {
    const container = document.getElementById('userContentList');
    if (!container) return;
    
    // 检查是否有刚完成的上传速度信息（Task 4）
    let speedHtml = '';
    if (sessionStorage.getItem('uploadJustCompleted') === 'true') {
        const speed = sessionStorage.getItem('lastUploadSpeed');
        if (speed) {
            speedHtml = `
                <div class="upload-speed-banner" id="uploadSpeedBanner">
                    <iconify-icon icon="lucide:check-circle" width="20" height="20" style="color: #22c55e;"></iconify-icon>
                    <span>上传完成！平均速度: <strong>${speed} MB/s</strong></span>
                    <button class="speed-banner-close" onclick="this.parentElement.remove()">
                        <iconify-icon icon="lucide:x" width="16" height="16"></iconify-icon>
                    </button>
                </div>
            `;
        }
        // 5秒后自动清除速度提示
        setTimeout(() => {
            const banner = document.getElementById('uploadSpeedBanner');
            if (banner) {
                banner.style.transition = 'opacity 0.5s ease';
                banner.style.opacity = '0';
                setTimeout(() => banner.remove(), 500);
            }
        }, 5000);
        sessionStorage.removeItem('uploadJustCompleted');
        sessionStorage.removeItem('lastUploadSpeed');
    }
    
    container.innerHTML = speedHtml + `
        <div style="text-align: center; padding: 60px;">
            <iconify-icon icon="tabler:loader-2" width="48" height="48" class="spin"></iconify-icon>
            <p style="margin-top: 16px; color: var(--text-secondary);">加载中...</p>
        </div>
    `;
    
    try {
        const token = localStorage.getItem('token');
        if (!token) {
            container.innerHTML = speedHtml + `
                <div style="text-align: center; padding: 60px;">
                    <iconify-icon icon="lucide:lock" width="64" height="64" style="opacity: 0.3;"></iconify-icon>
                    <h3 style="margin-top: 16px;">请先登录</h3>
                </div>
            `;
            return;
        }
        
        // 并行加载视频和图片
        const [videosRes, photosRes] = await Promise.all([
            fetch('/api/user/videos', { headers: { 'Authorization': 'Bearer ' + token } }),
            fetch('/api/user/photos', { headers: { 'Authorization': 'Bearer ' + token } })
        ]);
        
        const videos = videosRes.ok ? await videosRes.json() : [];
        const photos = photosRes.ok ? await photosRes.json() : [];
        
        // 合并并按时间排序
        const allContent = [
            ...videos.map(v => ({ ...v, type: 'video' })),
            ...photos.map(p => ({ ...p, type: 'photo' }))
        ].sort((a, b) => new Date(b.created_at) - new Date(a.created_at));
        
        // 检查是否有视频正在编码（Task 3）
        const hasEncodingVideos = videos.some(v => v.status === 'encoding');
        
        let encodingBannerHtml = '';
        if (hasEncodingVideos) {
            const encodingCount = videos.filter(v => v.status === 'encoding').length;
            encodingBannerHtml = `
                <div class="encoding-status-bar">
                    <div class="encoding-status-icon">
                        <iconify-icon icon="lucide:loader-2" width="20" height="20" class="spin"></iconify-icon>
                    </div>
                    <span class="encoding-status-text">${encodingCount} 个视频正在编码中</span>
                    <button class="encoding-refresh-btn" onclick="loadUserContent()">
                        <iconify-icon icon="lucide:refresh-cw" width="16" height="16"></iconify-icon>
                        刷新状态
                    </button>
                </div>
            `;
        }
        
        if (allContent.length === 0) {
            container.innerHTML = speedHtml + encodingBannerHtml + `
                <div style="text-align: center; padding: 60px;">
                    <iconify-icon icon="lucide:folder-open" width="64" height="64" style="opacity: 0.3;"></iconify-icon>
                    <h3 style="margin-top: 16px;">还没有上传内容</h3>
                    <p style="color: var(--text-secondary);">快去上传你的第一个作品吧！</p>
                    <button class="submit-btn" style="margin-top: 16px;" onclick="navigateTo('/upload')">
                        <iconify-icon icon="lucide:cloud-upload" style="vertical-align: middle; margin-right: 4px;"></iconify-icon>
                        去上传
                    </button>
                </div>
            `;
            return;
        }
        
        container.innerHTML = speedHtml + encodingBannerHtml + `<div class="content-grid">` + allContent.map((item, index) => {
            let statusClass = 'status-pending';
            let statusText = '待审核';
            let icon = item.type === 'video' ? 'tabler:video' : 'lucide:images';
            
            if (item.status === 'approved') {
                statusClass = 'status-approved';
                statusText = '已通过';
            } else if (item.status === 'rejected') {
                statusClass = 'status-rejected';
                statusText = '已拒绝';
            } else if (item.status === 'encoding') {
                statusClass = 'status-encoding';
                statusText = '<iconify-icon icon="lucide:loader-2" class="spin" width="14" height="14" style="vertical-align: middle;"></iconify-icon> 转码中';
            }
            
            // 视频编码状态徽章
            let encodeBadge = '';
            if (item.type === 'video') {
                if (item.status === 'encoding') {
                    encodeBadge = `
                        <span class="encode-badge encoding" id="encode-badge-${item.id}">
                            <iconify-icon icon="lucide:loader-2" width="12" height="12" class="spin"></iconify-icon>
                            <span class="encode-badge-text">编码中</span>
                            <span class="encode-badge-progress" id="encode-progress-text-${item.id}"></span>
                        </span>
                        <div class="encode-progress-wrap" id="encode-progress-wrap-${item.id}">
                            <div class="encode-progress-bar" id="encode-progress-bar-${item.id}"></div>
                        </div>
                    `;
                } else if (item.encoded === true || (item.qualities && item.qualities.length > 1)) {
                    encodeBadge = '<span class="encode-badge completed"><iconify-icon icon="lucide:check-circle" width="12" height="12"></iconify-icon> 已编码</span>';
                }
            }
            
            const thumbnail = item.type === 'video' 
                ? (item.coverFilename ? `/uploads/${item.coverFilename}` : null)
                : (item.photos && item.photos.length > 0 ? `/uploads/${item.photos[0].filename}` : null);
            
            return `
                <div class="content-item" data-video-id="${item.id}" style="animation: fadeInUp 0.5s ease ${index * 0.08}s both;">
                    <div class="content-thumbnail" style="background: ${thumbnail ? `url(${thumbnail}) center/cover` : 'linear-gradient(135deg, #667eea 0%, #764ba2 100%)'};">
                        ${!thumbnail ? `<iconify-icon icon="${icon}" width="48" height="48" style="opacity: 0.5;"></iconify-icon>` : ''}
                        <div class="content-type-badge">
                            <iconify-icon icon="${icon}" width="16" height="16"></iconify-icon>
                            <span>${item.type === 'video' ? '视频' : '图片'}</span>
                        </div>
                    </div>
                    <div class="content-info">
                        <h4>${escapeHtml(item.title)}</h4>
                        <p style="color: var(--text-secondary); font-size: 0.9rem;">${formatTimeAgo(item.created_at)}</p>
                        <div class="content-badges">
                            <span class="status-badge ${statusClass}" id="status-badge-${item.id}">${statusText}</span>
                            ${encodeBadge}
                        </div>
                        ${item.rejectReason ? `<p style="color: #ef4444; font-size: 0.85rem; margin-top: 4px;">原因: ${escapeHtml(item.rejectReason)}</p>` : ''}
                    </div>
                </div>
            `;
        }).join('') + `</div>`;
        
        // 如果有编码中的视频，启动轮询
        if (hasEncodingVideos) {
            startEncodingPolling(videos);
        }
        
    } catch (error) {
        console.error('加载用户内容失败:', error);
        container.innerHTML = speedHtml + `
            <div style="text-align: center; padding: 60px;">
                <iconify-icon icon="tabler:alert-circle" width="64" height="64" style="color: #ef4444;"></iconify-icon>
                <h3 style="margin-top: 16px;">加载失败</h3>
                <p style="color: var(--text-secondary);">请稍后重试</p>
            </div>
        `;
    }
}

// ==================== 转码进度轮询（Task 4） ====================
let encodingPollTimer = null;

function startEncodingPolling(videos) {
    // 清除旧轮询
    if (encodingPollTimer) {
        clearInterval(encodingPollTimer);
        encodingPollTimer = null;
    }

    async function pollTranscodeProgress() {
        // 如果页面已离开，停止轮询
        const container = document.getElementById('userContentList');
        if (!container || !container.isConnected) {
            clearInterval(encodingPollTimer);
            encodingPollTimer = null;
            return;
        }

        try {
            const token = localStorage.getItem('token');
            if (!token) {
                clearInterval(encodingPollTimer);
                encodingPollTimer = null;
                return;
            }

            const response = await fetch('/api/user/transcode/status', {
                headers: { 'Authorization': 'Bearer ' + token }
            });
            if (!response.ok) {
                if (response.status === 401 || response.status === 403) {
                    clearInterval(encodingPollTimer);
                    encodingPollTimer = null;
                }
                return;
            }
            const data = await response.json();
            const active = data.active || [];
            const recent = data.recent || [];

            // 检查是否有正在编码的视频已完成（出现在 recent 中或进度100%）
            let shouldRefresh = false;

            // 更新活动任务的进度
            for (const task of active) {
                const videoId = task.videoId;
                const progress = task.progress;

                // 更新状态文本显示百分比
                const statusBadge = document.getElementById('status-badge-' + videoId);
                if (statusBadge) {
                    statusBadge.innerHTML = '<iconify-icon icon="lucide:loader-2" class="spin" width="14" height="14" style="vertical-align: middle;"></iconify-icon> 转码中 ' + (progress != null ? progress + '%' : '...');
                }

                // 更新编码徽章文本
                const progressText = document.getElementById('encode-progress-text-' + videoId);
                if (progressText) {
                    progressText.textContent = progress != null ? progress + '%' : '';
                }

                // 更新进度条
                const progressBar = document.getElementById('encode-progress-bar-' + videoId);
                if (progressBar && progress != null) {
                    progressBar.style.width = Math.min(progress, 100) + '%';
                }

                // 进度达到100%，标记需要刷新
                if (progress != null && progress >= 100) {
                    shouldRefresh = true;
                }
            }

            // 检查当前编码的视频是否已出现在 recent 中（转码已完成）
            const encodingVideoIds = videos.filter(v => v.status === 'encoding').map(v => v.id);
            for (const task of recent) {
                if (encodingVideoIds.includes(task.videoId)) {
                    shouldRefresh = true;
                    break;
                }
            }

            if (shouldRefresh) {
                clearInterval(encodingPollTimer);
                encodingPollTimer = null;
                loadUserContent();
            }
        } catch (e) {
            console.warn('Poll transcode progress failed:', e.message);
        }
    }

    // 立即执行一次
    pollTranscodeProgress();
    // 每5秒轮询
    encodingPollTimer = setInterval(pollTranscodeProgress, 5000);
}

