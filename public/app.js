// 处理图片选择
function handlePhotosSelect(files) {
    if (files.length === 0) return;
    
    if (files.length > 50) {
        showToast('一次最多只能上传50张图片', 'warning');
        files = files.slice(0, 50);
    }
    
    selectedPhotos = files;
    
    // 显示预览
    renderPhotoPreviews();
    
    // 显示选择信息
    document.getElementById('selectedPhotosInfo').style.display = 'block';
    document.getElementById('selectedPhotosCount').textContent = files.length;
}

// 渲染图片预览
function renderPhotoPreviews() {
    const previewContainer = document.getElementById('photosPreview');
    const placeholder = document.getElementById('photosPlaceholder');
    
    if (selectedPhotos.length === 0) {
        placeholder.classList.remove('hidden');
        previewContainer.classList.add('hidden');
        document.getElementById('selectedPhotosInfo').style.display = 'none';
        return;
    }
    
    placeholder.classList.add('hidden');
    previewContainer.classList.remove('hidden');
    
    previewContainer.innerHTML = selectedPhotos.map((file, index) => `
        <div class="photo-preview-item" style="position: relative; aspect-ratio: 1; border-radius: 8px; overflow: hidden;" data-index="${index}">
            <img src="${URL.createObjectURL(file)}" style="width: 100%; height: 100%; object-fit: cover;">
            <div style="position: absolute; top: 4px; right: 4px; background: rgba(0,0,0,0.6); color: white; padding: 2px 6px; border-radius: 4px; font-size: 12px;">${index + 1}</div>
            <button type="button" class="photo-delete-btn" onclick="deleteSelectedPhoto(${index})" title="删除此图片" style="position: absolute; top: 4px; left: 4px; background: rgba(239, 68, 68, 0.9); color: white; border: none; border-radius: 4px; padding: 4px; cursor: pointer; display: flex; align-items: center; justify-content: center; transition: all 0.2s ease; opacity: 0; transform: scale(0.8);">
                <iconify-icon icon="lucide:x" width="16" height="16"></iconify-icon>
            </button>
        </div>
    `).join('');
    
    // 添加悬停效果
    previewContainer.querySelectorAll('.photo-preview-item').forEach(item => {
        item.addEventListener('mouseenter', () => {
            const deleteBtn = item.querySelector('.photo-delete-btn');
            if (deleteBtn) {
                deleteBtn.style.opacity = '1';
                deleteBtn.style.transform = 'scale(1)';
            }
        });
        item.addEventListener('mouseleave', () => {
            const deleteBtn = item.querySelector('.photo-delete-btn');
            if (deleteBtn) {
                deleteBtn.style.opacity = '0';
                deleteBtn.style.transform = 'scale(0.8)';
            }
        });
    });
}

// 删除已选择的单个图片
function deleteSelectedPhoto(index) {
    if (index < 0 || index >= selectedPhotos.length) {
        console.error('无效的图片索引:', index);
        return;
    }
    
    // 显示确认对话框
    if (!confirm(`确定要删除第 ${index + 1} 张图片吗？\n\n文件名: ${selectedPhotos[index].name}`)) {
        return;
    }
    
    try {
        // 从数组中删除指定索引的图片
        selectedPhotos.splice(index, 1);
        
        // 重新渲染预览
        renderPhotoPreviews();
        
        // 更新选择信息
        document.getElementById('selectedPhotosCount').textContent = selectedPhotos.length;
        
        if (selectedPhotos.length === 0) {
            document.getElementById('selectedPhotosInfo').style.display = 'none';
            // 清空文件输入框
            const photosFile = document.getElementById('photosFile');
            if (photosFile) {
                photosFile.value = '';
            }
        }
        
        showToast('图片已删除', 'success');
    } catch (error) {
        console.error('删除图片失败:', error);
        showToast('删除失败，请重试', 'error');
    }
}

// 处理图片上传
async function handlePhotoUpload(event) {
    event.preventDefault();
    
    if (selectedPhotos.length === 0) {
        showToast('请选择至少一张图片', 'error');
        return;
    }
    
    const token = localStorage.getItem('token');
    if (!token) {
        showToast('请先登录', 'error');
        showPage('auth');
        return;
    }
    
    const formData = new FormData();
    selectedPhotos.forEach(file => {
        formData.append('photos', file);
    });
    formData.append('title', document.getElementById('photoTitle').value);
    formData.append('description', document.getElementById('photoDescription').value);
    formData.append('category', document.getElementById('photoCategory').value);
    formData.append('tags', document.getElementById('photoTags').value);
    
    const uploadBtn = document.getElementById('photoUploadBtn');
    const btnText = uploadBtn.querySelector('.btn-text');
    const btnLoading = uploadBtn.querySelector('.btn-loading');
    
    uploadBtn.disabled = true;
    btnText.classList.add('hidden');
    btnLoading.classList.remove('hidden');
    
    showPhotoUploadProgress(true, selectedPhotos.length);
    
    try {
        const response = await fetch('/api/photos/batch', {
            method: 'POST',
            headers: {
                'Authorization': 'Bearer ' + token
            },
            body: formData
        });
        
        const result = await response.json();
        
        if (response.ok) {
            showPhotoUploadResult(result.count);
            showToast(`成功上传 ${result.count} 张图片，等待审核`, 'success');
            
            document.getElementById('photoUploadForm').reset();
            selectedPhotos = [];
            document.getElementById('photosPreview').innerHTML = '';
            document.getElementById('photosPreview').classList.add('hidden');
            document.getElementById('photosPlaceholder').classList.remove('hidden');
            document.getElementById('selectedPhotosInfo').style.display = 'none';
            document.getElementById('photoTitleCount').textContent = '0/100';
            document.getElementById('photoDescCount').textContent = '0/500';
        } else {
            throw new Error(result.error || '上传失败');
        }
    } catch (error) {
        console.error('上传失败:', error);
        showUploadError(error.message || '上传失败，请重试', '请检查网络连接后重试');
        showToast(error.message || '上传失败，请重试', 'error');
    } finally {
        uploadBtn.disabled = false;
        btnText.classList.remove('hidden');
        btnLoading.classList.add('hidden');
    }
}

