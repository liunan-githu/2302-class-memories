function formatDate(dateStr) {
    const date = new Date(dateStr);
    const now = new Date();
    const diff = now - date;
    
    const minutes = Math.floor(diff / (1000 * 60));
    const hours = Math.floor(diff / (1000 * 60 * 60));
    const days = Math.floor(diff / (1000 * 60 * 60 * 24));
    
    if (minutes < 1) return '刚刚';
    if (minutes < 60) return `${minutes}分钟前`;
    if (hours < 24) return `${hours}小时前`;
    if (days < 7) return `${days}天前`;
    
    return date.toLocaleDateString('zh-CN');
}

async function handleCommentSubmit(event) {
    event.preventDefault();
    
    // 先检查用户状态
    if (currentUser && currentUser.isBanned) {
        handleUserBanned(currentUser.banReason);
        return;
    }
    
    const commentInput = document.getElementById('commentInput');
    const content = commentInput.value.trim();
    
    if (!content) {
        showToast('评论内容不能为空', 'error');
        return;
    }
    
    if (!currentVideoId) {
        showToast('加载失败，请刷新页面', 'error');
        return;
    }
    
    const submitBtn = event.target.querySelector('.comment-submit-btn');
    submitBtn.disabled = true;
    
    try {
        const response = await fetch(`/api/videos/${currentVideoId}/comments`, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'Authorization': `Bearer ${authToken}`
            },
            body: JSON.stringify({ content })
        });
        
        if (response.ok) {
            const newComment = await response.json();
            showToast('评论发表成功！', 'success');
            commentInput.value = '';
            document.getElementById('commentCount').textContent = '0/500';
            loadComments(currentVideoId);
        } else {
            const data = await response.json();
            showToast(data.error || '评论失败', 'error');
        }
    } catch (error) {
        showToast('评论失败，请重试', 'error');
    } finally {
        submitBtn.disabled = false;
    }
}

