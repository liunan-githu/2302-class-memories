function validateRegistration() {
    const username = document.getElementById('registerUsername').value;
    const password = document.getElementById('registerPassword').value;
    const confirmPassword = document.getElementById('registerPasswordConfirm').value;
    const registerBtn = document.getElementById('registerBtn');
    
    let isValid = true;
    
    // 用户名验证
    const usernameHint = document.getElementById('usernameHint');
    if (username.length < 3) {
        usernameHint.textContent = '✗ 用户名至少需要3个字符';
        usernameHint.className = 'validation-hint error';
        isValid = false;
    } else if (username.length > 20) {
        usernameHint.textContent = '✗ 用户名不能超过20个字符';
        usernameHint.className = 'validation-hint error';
        isValid = false;
    } else if (!/^[a-zA-Z0-9_\u4e00-\u9fa5]+$/.test(username)) {
        usernameHint.textContent = '✗ 用户名只能包含字母、数字、下划线和中文';
        usernameHint.className = 'validation-hint error';
        isValid = false;
    } else {
        usernameHint.textContent = '✓ 用户名可用';
        usernameHint.className = 'validation-hint success';
    }
    
    // 密码强度验证
    const passwordHint = document.getElementById('passwordHint');
    const strengthFill = document.getElementById('strengthFill');
    const strengthText = document.getElementById('strengthText');
    let strength = 0;
    
    if (password.length >= 6) {
        strength += 25;
        if (password.length >= 10) strength += 15;
    }
    if (/[a-z]/.test(password)) strength += 15;
    if (/[A-Z]/.test(password)) strength += 15;
    if (/[0-9]/.test(password)) strength += 15;
    if (/[^a-zA-Z0-9]/.test(password)) strength += 15;
    
    strength = Math.min(100, strength);
    
    if (password.length < 6) {
        passwordHint.textContent = '✗ 密码至少需要6个字符';
        passwordHint.className = 'validation-hint error';
        isValid = false;
    } else if (password.length > 30) {
        passwordHint.textContent = '✗ 密码不能超过30个字符';
        passwordHint.className = 'validation-hint error';
        isValid = false;
    } else {
        passwordHint.textContent = '✓ 密码格式正确';
        passwordHint.className = 'validation-hint success';
    }
    
    // 更新密码强度条
    strengthFill.style.width = strength + '%';
    if (strength < 30) {
        strengthFill.style.backgroundColor = '#ef4444';
        strengthText.textContent = '弱';
        strengthText.style.color = '#ef4444';
    } else if (strength < 60) {
        strengthFill.style.backgroundColor = '#f59e0b';
        strengthText.textContent = '中';
        strengthText.style.color = '#f59e0b';
    } else if (strength < 85) {
        strengthFill.style.backgroundColor = '#3b82f6';
        strengthText.textContent = '强';
        strengthText.style.color = '#3b82f6';
    } else {
        strengthFill.style.backgroundColor = '#10b981';
        strengthText.textContent = '极强';
        strengthText.style.color = '#10b981';
    }
    
    // 确认密码验证
    const confirmHint = document.getElementById('passwordConfirmHint');
    if (!confirmPassword) {
        confirmHint.textContent = '';
        isValid = false;
    } else if (password !== confirmPassword) {
        confirmHint.textContent = '✗ 两次密码不一致';
        confirmHint.className = 'validation-hint error';
        isValid = false;
    } else {
        confirmHint.textContent = '✓ 密码一致';
        confirmHint.className = 'validation-hint success';
    }
    
    registerBtn.disabled = !isValid;
    return isValid;
}

async function handleRegister(event) {
    event.preventDefault();
    
    if (!validateRegistration()) {
        showToast('请检查输入内容', 'error');
        return;
    }
    
    const btn = document.getElementById('registerBtn');
    btn.disabled = true;
    
    const username = document.getElementById('registerUsername').value;
    const password = document.getElementById('registerPassword').value;
    
    try {
        const response = await fetch('/api/register', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ username, password })
        });
        
        const data = await response.json();
        
        if (response.ok) {
            showToast('注册成功，请登录！', 'success');
            document.getElementById('registerForm').querySelector('form').reset();
            document.getElementById('passwordStrength').style.opacity = '0';
            navigateTo('/login');
        } else {
            showToast(data.error || '注册失败', 'error');
        }
    } catch (error) {
        showToast('注册失败，请重试', 'error');
    } finally {
        btn.disabled = true;
    }
}

async function handleLogin(event) {
    event.preventDefault();
    
    const btn = document.getElementById('loginBtn');
    btn.disabled = true;
    
    const username = document.getElementById('loginUsername').value;
    const password = document.getElementById('loginPassword').value;
    
    try {
        const response = await fetch('/api/login', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ username, password })
        });
        
        const data = await response.json();
        
        if (response.ok) {
            currentUser = data.user;
            authToken = data.token;
            localStorage.setItem('user', JSON.stringify(currentUser));
            localStorage.setItem('token', authToken);
            updateNav();
            updateGuestBanner();
            updateCommentForm();
            showToast('登录成功！', 'success');
            
            // 启动用户状态检查
            startUserStatusCheck();
            
            // 如果是管理员或超级管理员，跳转到后台
            if (currentUser.role === 'admin' || currentUser.role === 'superadmin') {
                window.location.href = '/admin';
            } else {
                navigateTo('/');
            }
        } else {
            if (data.isBanned) {
                showToast(`${data.error}: ${data.banReason || '无原因'}`, 'error');
            } else {
                showToast(data.error || '登录失败', 'error');
            }
        }
    } catch (error) {
        showToast('登录失败，请重试', 'error');
    } finally {
        btn.disabled = false;
    }
}

function handleLogout() {
    stopUserStatusCheck();
    currentUser = null;
    authToken = null;
    localStorage.removeItem('user');
    localStorage.removeItem('token');
    updateNav();
    updateGuestBanner();
    updateCommentForm();
    showToast('已退出登录', 'info');
    navigateTo('/');
}

