function applyClassmatesFilters() {
    const searchValue = document.getElementById('classmatesSearch')?.value.toLowerCase() || '';
    const yearFilter = document.getElementById('classmatesYearFilter')?.value || '';
    const sortBy = document.getElementById('classmatesSort')?.value || 'newest';
    
    filteredClassmates = allClassmates.filter(c => {
        const matchesSearch = !searchValue || 
            c.name.toLowerCase().includes(searchValue) ||
            (c.personality && c.personality.toLowerCase().includes(searchValue)) ||
            (c.description && c.description.toLowerCase().includes(searchValue)) ||
            (c.contact && c.contact.toLowerCase().includes(searchValue));
        
        const matchesYear = !yearFilter || c.graduationYear === yearFilter;
        
        return matchesSearch && matchesYear;
    });
    
    if (sortBy === 'name') {
        filteredClassmates.sort((a, b) => a.name.localeCompare(b.name, 'zh-CN'));
    } else if (sortBy === 'graduationYear') {
        filteredClassmates.sort((a, b) => (b.graduationYear || '').localeCompare(a.graduationYear || '', 'zh-CN'));
    } else {
        filteredClassmates.sort((a, b) => new Date(b.created_at) - new Date(a.created_at));
    }
    
    const resultsInfo = document.getElementById('classmatesResultsInfo');
    const resultsCount = document.getElementById('classmatesResultsCount');
    if (searchValue || yearFilter) {
        resultsInfo.style.display = 'flex';
        resultsCount.textContent = filteredClassmates.length;
    } else {
        resultsInfo.style.display = 'none';
    }
    
    renderClassmates(filteredClassmates);
}

