async function handleUpload(event) {
    event.preventDefault();
    
    if (isUploading) {
        showToast('正在上传中，请稍候...', 'warning');
        return;
    }
    
    if (currentUser && currentUser.isBanned) {
        handleUserBanned(currentUser.banReason);
        return;
    }
    
    if (currentUser && currentUser.uploadDisabled) {
        showToast(`⚠️ 您的上传功能已被禁用: ${currentUser.uploadDisableReason || '无原因'}`, 'error');
        return;
    }
    
    const videoInput = document.getElementById('videoFile');
    if (!videoInput.files || videoInput.files.length === 0) {
        showToast('请选择要上传的视频文件', 'warning');
        return;
    }
    
    isUploading = true;
    setUploadButtonState(true);
    showUploadProgress(true);
    
    const videoFile = videoInput.files[0];
    document.getElementById('uploadFileName').textContent = videoFile.name;
    document.getElementById('uploadFileSize').textContent = formatFileSize(videoFile.size);
    
    const formData = new FormData();
    formData.append('title', document.getElementById('videoTitle').value);
    formData.append('description', document.getElementById('videoDescription').value);
    formData.append('category', document.getElementById('videoCategory').value);
    formData.append('tags', document.getElementById('videoTags').value);
    formData.append('privacy', document.getElementById('videoPrivacy').value);
    formData.append('allowComments', document.getElementById('allowComments').value);
    formData.append('video', videoFile);
    
    const coverInput = document.getElementById('coverFile');
    if (coverInput.files && coverInput.files.length > 0) {
        formData.append('cover', coverInput.files[0]);
    } else if (autoGeneratedCover) {
        formData.append('cover', autoGeneratedCover);
    }
    
    lastUploadFormData = formData;
    
    const startTime = Date.now();
    
    let lastUploadSpeed = 0;

    return new Promise((resolve, reject) => {
        const xhr = new XMLHttpRequest();
        
        xhr.upload.addEventListener('progress', (e) => {
            if (e.lengthComputable) {
                const percent = (e.loaded / e.total) * 100;
                const elapsed = (Date.now() - startTime) / 1000;
                lastUploadSpeed = elapsed > 0 ? (e.loaded / (1024 * 1024)) / elapsed : 0;
                updateUploadProgress(percent, lastUploadSpeed, '正在上传...');
            }
        });
        
        xhr.addEventListener('load', () => {
            try {
                const data = JSON.parse(xhr.responseText);
                
                if (xhr.status >= 200 && xhr.status < 300) {
                    updateUploadProgress(100, 0, '上传完成！');
                    document.getElementById('uploadProgressStatus').textContent = '上传成功！';
                    showToast('回忆上传成功，等待审核！', 'success');

                    sessionStorage.setItem('lastUploadSpeed', lastUploadSpeed.toFixed(2));
                    sessionStorage.setItem('uploadJustCompleted', 'true');

                    closeUploadDetailModal();

                    setTimeout(() => navigateTo('/upload/status'), 300);
                    resolve();
                } else {
                    showUploadError(data.error || '上传失败', data.uploadDisabled ? data.reason : null);
                    if (data.uploadDisabled) {
                        showToast(`${data.error}: ${data.reason || '无原因'}`, 'error');
                    } else {
                        showToast(data.error || '上传失败', 'error');
                    }
                    reject();
                }
            } catch (error) {
                showUploadError('解析响应失败', '服务器返回了无法解析的响应');
                showToast('上传失败，请重试', 'error');
                reject();
            } finally {
                isUploading = false;
                setUploadButtonState(false);
            }
        });
        
        xhr.addEventListener('error', () => {
            showUploadError('网络错误', '请检查网络连接后重试');
            showToast('上传失败，请重试', 'error');
            isUploading = false;
            setUploadButtonState(false);
            reject();
        });
        
        xhr.addEventListener('abort', () => {
            document.getElementById('uploadDetailModal').classList.add('hidden');
            showToast('上传已取消', 'warning');
            isUploading = false;
            setUploadButtonState(false);
            reject();
        });
        
        xhr.open('POST', '/api/upload');
        xhr.setRequestHeader('Authorization', 'Bearer ' + authToken);
        xhr.send(formData);
    });
}

