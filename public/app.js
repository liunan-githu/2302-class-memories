async function checkUserStatus() {
    console.log('正在检查用户状态...');
    if (!currentUser || !authToken) {
        console.log('未登录，跳过状态检查');
        return;
    }
    
    try {
        console.log('发送状态检查请求...');
        const response = await fetch('/api/user/status', {
            headers: { 'Authorization': 'Bearer ' + authToken }
        });
        
        console.log('状态检查响应:', response.status);
        
        if (!response.ok) {
            // 如果是403，说明被封禁了
            if (response.status === 403) {
                try {
                    const data = await response.json();
                    console.log('封禁数据:', data);
                    if (data.isBanned) {
                        handleUserBanned(data.banReason);
                    }
                } catch (e) {
                    console.error('解析封禁响应失败:', e);
                }
            }
            return;
        }
        
        const data = await response.json();
        console.log('用户状态数据:', data);
        
        // 更新本地用户状态
        currentUser.isBanned = data.isBanned;
        currentUser.banReason = data.banReason;
        currentUser.uploadDisabled = data.uploadDisabled;
        currentUser.uploadDisableReason = data.uploadDisableReason;
        localStorage.setItem('user', JSON.stringify(currentUser));
        
        if (data.isBanned) {
            console.log('用户已被封禁，处理中...');
            handleUserBanned(data.banReason);
        } else if (data.uploadDisabled) {
            showToast(`⚠️ 您的上传功能已被禁用: ${data.uploadDisableReason || '无原因'}`, 'warning');
        }
    } catch (error) {
        console.error('检查用户状态失败:', error);
    }
}

function handleUserBanned(banReason) {
    if (userStatusCheckInterval) {
        clearInterval(userStatusCheckInterval);
        userStatusCheckInterval = null;
    }
    
    showToast(`⚠️ 您的账号已被封禁: ${banReason || '无原因'}，3秒后将自动退出...`, 'error');
    
    // 3秒后自动退出登录
    setTimeout(() => {
        handleLogout();
    }, 3000);
}

function startUserStatusCheck() {
    console.log('启动用户状态检查');
    
    // 先检查一次
    checkUserStatus();
    
    // 3秒后再次检查
    setTimeout(() => {
        checkUserStatus();
    }, 3000);
    
    // 每10秒检查一次
    if (userStatusCheckInterval) {
        clearInterval(userStatusCheckInterval);
    }
    userStatusCheckInterval = setInterval(checkUserStatus, 10000);
    console.log('用户状态检查定时器已设置，ID:', userStatusCheckInterval);
}

function stopUserStatusCheck() {
    if (userStatusCheckInterval) {
        clearInterval(userStatusCheckInterval);
        userStatusCheckInterval = null;
    }
}

