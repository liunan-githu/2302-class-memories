// 更新图片查看器 - 修复版
function updatePhotoViewer() {
    if (!currentPhotoBatch || !currentPhotoBatch.photos[currentPhotoIndex]) return;
    
    const photo = currentPhotoBatch.photos[currentPhotoIndex];
    const img = document.getElementById('currentPhoto');
    const mobileImg = document.getElementById('mobileCurrentPhoto');
    
    // 更新桌面端
    if (img) {
        if (img.classList.contains('switching-out') || img.classList.contains('switching-in')) {
            return;
        }
        img.classList.add('switching-out');
    }
    
    // 更新手机端（直接切换，无动画避免冲突）
    if (mobileImg) {
        mobileImg.src = '/uploads/' + photo.filename;
        mobileImg.alt = photo.originalName || '图片';
    }
    
    // 更新手机端计数器
    const mobileIdxEl = document.getElementById('mobileCurrentPhotoIndex');
    const mobileTotalEl = document.getElementById('mobileTotalPhotos');
    if (mobileIdxEl) mobileIdxEl.textContent = currentPhotoIndex + 1;
    if (mobileTotalEl) mobileTotalEl.textContent = currentPhotoBatch.photos.length;
    
    // 更新桌面端计数器
    const idxEl = document.getElementById('currentPhotoIndex');
    const totalEl = document.getElementById('totalPhotos');
    if (idxEl) idxEl.textContent = currentPhotoIndex + 1;
    if (totalEl) totalEl.textContent = currentPhotoBatch.photos.length;
    
    // 桌面端淡出动画后更新
    if (img) {
        setTimeout(() => {
            const newImg = new Image();
            newImg.src = `/uploads/${photo.filename}`;
            
            newImg.onload = () => {
                img.src = newImg.src;
                img.alt = photo.originalName || '图片';
                
                img.classList.remove('switching-out');
                img.classList.add('switching-in');
                
                setTimeout(() => {
                    img.classList.remove('switching-in');
                }, 300);
            };
            
            newImg.onerror = () => {
                img.src = 'data:image/svg+xml,<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 400 300"><rect fill="%23333" width="400" height="300"/><text fill="%23888" x="50%" y="50%" text-anchor="middle" dy=".3em">加载失败</text></svg>';
                img.classList.remove('switching-out');
                img.classList.add('switching-in');
                setTimeout(() => {
                    img.classList.remove('switching-in');
                }, 300);
            };
        }, 250);
    }
    
    // 更新导航按钮状态
    updateNavButtons();
    
    // 更新缩略图激活状态
    updateThumbnails();
}

// 更新导航按钮状态
function updateNavButtons() {
    const isFirst = currentPhotoIndex === 0;
    const isLast = currentPhotoIndex === currentPhotoBatch.photos.length - 1;
    
    document.querySelectorAll('.photo-nav-btn.prev').forEach(btn => {
        btn.disabled = isFirst;
        btn.style.opacity = isFirst ? '0.2' : '1';
        btn.style.pointerEvents = isFirst ? 'none' : 'auto';
    });
    
    document.querySelectorAll('.photo-nav-btn.next').forEach(btn => {
        btn.disabled = isLast;
        btn.style.opacity = isLast ? '0.2' : '1';
        btn.style.pointerEvents = isLast ? 'none' : 'auto';
    });
}

// 更新缩略图状态
function updateThumbnails() {
    document.querySelectorAll('.photo-thumbnail').forEach((thumb, index) => {
        thumb.classList.toggle('active', index === currentPhotoIndex);
        // 滚动到当前缩略图
        if (index === currentPhotoIndex) {
            thumb.scrollIntoView({ behavior: 'smooth', inline: 'center', block: 'nearest' });
        }
    });
}

// 渲染缩略图
function renderPhotoThumbnails() {
    const container = document.getElementById('photoThumbnails');
    if (!container || !currentPhotoBatch) return;
    
    container.innerHTML = currentPhotoBatch.photos.map((photo, index) => `
        <div class="photo-thumbnail ${index === 0 ? 'active' : ''}" onclick="goToPhoto(${index})">
            <img src="/uploads/${photo.filename}" alt="">
        </div>
    `).join('');
}

// 切换锁定标志，防止快速点击
let isSwitching = false;

// 上一张图片
function prevPhoto() {
    if (isSwitching || !currentPhotoBatch || currentPhotoIndex <= 0) return;
    
    isSwitching = true;
    currentPhotoIndex--;
    updatePhotoViewer();
    
    // 动画完成后解锁
    setTimeout(() => {
        isSwitching = false;
    }, 600);
}

// 下一张图片
function nextPhoto() {
    if (isSwitching || !currentPhotoBatch || currentPhotoIndex >= currentPhotoBatch.photos.length - 1) return;
    
    isSwitching = true;
    currentPhotoIndex++;
    updatePhotoViewer();
    
    // 动画完成后解锁
    setTimeout(() => {
        isSwitching = false;
    }, 600);
}

// 跳转到指定图片
function goToPhoto(index) {
    if (currentPhotoBatch && index >= 0 && index < currentPhotoBatch.photos.length) {
        currentPhotoIndex = index;
        updatePhotoViewer();
    }
}

