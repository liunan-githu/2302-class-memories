let bottomSheetOverlay = null;
let bottomSheetContent = null;
let bottomSheetDragStartY = 0;
let bottomSheetIsDragging = false;

function openBottomSheet(contentElement, options = {}) {
    closeBottomSheet();

    const overlay = document.createElement('div');
    overlay.className = 'bottom-sheet-overlay';

    const sheet = document.createElement('div');
    sheet.className = 'bottom-sheet';
    if (options.fullScreen) {
        sheet.style.maxHeight = '100vh';
        sheet.style.height = '100vh';
        sheet.style.borderRadius = '0';
    }

    const handle = document.createElement('div');
    handle.className = 'bottom-sheet-handle';
    sheet.appendChild(handle);

    const contentWrapper = document.createElement('div');
    contentWrapper.className = 'bottom-sheet-body';
    if (typeof contentElement === 'string') {
        contentWrapper.innerHTML = contentElement;
    } else {
        contentWrapper.appendChild(contentElement);
    }
    sheet.appendChild(contentWrapper);

    overlay.appendChild(sheet);
    document.body.appendChild(overlay);

    const handleDragStart = (e) => {
        bottomSheetDragStartY = e.touches[0].clientY;
        bottomSheetIsDragging = true;
        sheet.style.transition = 'none';
    };

    const handleDragMove = (e) => {
        if (!bottomSheetIsDragging) return;
        const diff = e.touches[0].clientY - bottomSheetDragStartY;
        if (diff > 0) {
            sheet.style.transform = 'translateY(' + diff + 'px)';
        }
    };

    const handleDragEnd = (e) => {
        if (!bottomSheetIsDragging) return;
        bottomSheetIsDragging = false;
        const diff = e.changedTouches[0].clientY - bottomSheetDragStartY;
        sheet.style.transition = '';
        sheet.style.transform = '';
        if (diff > 100) {
            closeBottomSheet();
        }
        bottomSheetDragStartY = 0;
    };

    sheet.addEventListener('touchstart', handleDragStart, { passive: true });
    document.addEventListener('touchmove', handleDragMove, { passive: true });
    document.addEventListener('touchend', handleDragEnd);

    overlay.addEventListener('click', (e) => {
        if (e.target === overlay) {
            closeBottomSheet();
        }
    });

    overlay._cleanup = function() {
        sheet.removeEventListener('touchstart', handleDragStart);
        document.removeEventListener('touchmove', handleDragMove);
        document.removeEventListener('touchend', handleDragEnd);
    };

    bottomSheetOverlay = overlay;
    bottomSheetContent = sheet;

    requestAnimationFrame(function() {
        overlay.classList.add('active');
        sheet.classList.add('active');
    });

    return { overlay: overlay, sheet: sheet };
}

function closeBottomSheet() {
    if (bottomSheetOverlay) {
        bottomSheetOverlay.classList.remove('active');
        if (bottomSheetContent) {
            bottomSheetContent.classList.remove('active');
        }
        if (bottomSheetOverlay._cleanup) {
            bottomSheetOverlay._cleanup();
        }
        setTimeout(function() {
            if (bottomSheetOverlay && bottomSheetOverlay.parentNode) {
                bottomSheetOverlay.parentNode.removeChild(bottomSheetOverlay);
            }
            bottomSheetOverlay = null;
            bottomSheetContent = null;
        }, 300);
    }
}

/* 主题相关功能 */
function initTheme() {
    const savedTheme = localStorage.getItem('theme') || 'light';
    setTheme(savedTheme);
}

function setTheme(theme) {
    const html = document.documentElement;
    const themeIcon = document.getElementById('themeIcon');
    
    if (theme === 'dark') {
        html.setAttribute('data-theme', 'dark');
        if (themeIcon) {
            themeIcon.setAttribute('icon', 'lucide:moon');
        }
    } else {
        html.removeAttribute('data-theme');
        if (themeIcon) {
            themeIcon.setAttribute('icon', 'lucide:sun');
        }
    }
    
    localStorage.setItem('theme', theme);
}

function toggleTheme() {
    const currentTheme = document.documentElement.getAttribute('data-theme');
    const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
    setTheme(newTheme);
}

